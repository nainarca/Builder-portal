const series = (base: number, variance: number): { label: string; value: number }[] =>
  ['00', '04', '08', '12', '16', '20', '24'].map((h, i) => ({
    label: `${h}:00`,
    value: Math.max(0, Math.round(base + Math.sin(i) * variance + i * 2)),
  }));

